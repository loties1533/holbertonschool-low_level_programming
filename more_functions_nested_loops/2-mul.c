#include "main.h"

/**
 * mul - multiplie deux entiers
 * Description: Cette fonction prend deux entiers en paramètres
 * et retourne le résultat de leur multiplication.
 * @a: le premier entier
 * @b: le deuxième entier
 *
 * Return: le résultat de la multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}